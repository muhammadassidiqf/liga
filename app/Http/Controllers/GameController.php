    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'mdate' => 'required',
            'team1' => 'required',
            'team2' => 'required',
            'stadium' => 'required'
        ]);
        $data = [
            'mdate' => $request->mdate,
            'team1' => $request->team1,
            'team2' => $request->team2,
            'stadium' => $request->stadium
        ];
        Game::create($data);
        return response([
            'success' => true,
            'message' => 'Berhasil store game',
            'data' => $data
        ], 200);
    }

